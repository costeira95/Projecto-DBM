var express = require('express');
var router = express.Router();

router.get('/Aluno',function(req,res) {
    res.send('View: Backoffice Aluno');
});
router.get('/Aluno/detalhe/:id',function(req,res) {
    res.send('View: Backoffice Aluno Detalhe');
});
router.get('/Turma',function(req,res) {
    res.send('View: Backoffice Turma');
});
router.get('/Turma/detalhe/:id',function(req,res) {
    res.send('View: Backoffice Turma Detalhe');
});

module.exports = router;
